package Week_2_Loops_arrays;

public class ForLoops {
    public static void main(String[] args) {

        // Print numbers 1-5
        for (int x = 0; x < 5; x++){
            System.out.println(x);
        }

        // Print count down 10, 9, 8... 0
        for (int y = 10;  y >= 0; y--){
            System.out.println(y);
        }

    }
}
